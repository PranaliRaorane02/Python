#Basic Math Programs

#Average of 3 Numbers
a1= int(input("Enter the first number:"))
b1= int(input("Enter the second number:"))
c1= int(input("Enter the third number:"))
avg= (a1+b1+c1)/3
print("Average:",avg)

#Square of a Number
a2= int(input("Enter A Number:"))
